        <p>
          Colony collapse disorder (CCD) is a phenomenon in which worker bees from a beehive abruptly disappear. Since CCD was identified in 2006, scientists around the world have been trying to determine the cause. Many have been proposed:  A multitude of issues such as climate change, environmental changes, herbicides, very powerful pesticides, foreign species such as Varroa Mites, Hive Beetles, fungus, pesticides &amp; even beekeeping practices (such as the use of antibiotics, or long-distance transportation of beehives). We believe it's all these things combined that are overwhelming bees.
